    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String numeroInscripcion = this.TxtNumeroDeInscripcion.getText();
            String nombreEstudiante = this.TxtNombreEstudiante.getText();
            Integer patrimonioEstudiante = Integer.parseInt(this.TxtPatrimonioEstudiante.getText());
            Integer estratoEstudiante = Integer.parseInt(this.TxtEstratoEstudiante.getText());
            
            Double valorMatricula = (double)50000;
            
            if ((patrimonioEstudiante > 2000000) && (estratoEstudiante >3)) {
                valorMatricula = valorMatricula + (int)(0.03 * patrimonioEstudiante);
            }
            
            String mensajeFinal = "El estudiante con numero de inscripcion: : " + numeroInscripcion + "\n" +
                              "Y nombre: " + nombreEstudiante + "\n" +
                              "Debe pagar: " + valorMatricula ;
                              
        
            JOptionPane.showMessageDialog(null, mensajeFinal);
            
        } catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Se deben introducir valores numericos validos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

